// Shared CORS headers for the Notebook Edge Functions.
// The mobile client calls these via supabase-js functions.invoke (authenticated with
// the user's session); CORS is permissive because there is no browser origin to pin.

export const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
  'Access-Control-Allow-Methods': 'POST, OPTIONS',
};

export function json(body: unknown, status = 200): Response {
  return new Response(JSON.stringify(body), {
    status,
    headers: { ...corsHeaders, 'content-type': 'application/json' },
  });
}
